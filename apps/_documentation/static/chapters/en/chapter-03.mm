## Dashboard

Login into the dashboard

<img src="screenshots/dashboard_login.png" />

Click on a tab title to expand. Tabs are context dependent. For example, open tab "Installed Applications" and click on an installed application to select it. This will create new tabs "Routes", "Filed", and "Model" for the selected app.

<img src="screenshots/dashboard_main.png" />

The "Files" tab allows you to browse the folder that contains the selected app and edit any file that comprises the app. If you edit a file you must click on "Reload Apps" under the "Installed Applications" tab for the change to take effect. If an app fails to load, its corresponding button is displayed in red. Click on it to see the corresponding error.

<img src="screenshots/dashboard_edit.png" />

The Dashboard exposes the db of all the apps using pydal RESTAPI. It also provides a web interface to perform search and CRUD operations.

<img src="screenshots/dashboard_restapi.png" />

If a user visits and app and triggers a bug, the user it issued a ticket.

<img src="screenshots/dashboard_error.png" />

The ticket is logged in py4web database. The Dashboard displays th most commoon recent issues and allows searching tickets.

<img src="screenshots/dashboard_ticket.png" />

